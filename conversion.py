def BitToBytes(b) -> bytes:
    if len(b) % 8 != 0:
        raise ValueError(f"Le tableau de bits n'a pas une longueur multiple de 8")
    
    l = len(b)
    B = [0] * ( l//8 )
    for i in range(l):
        B[i//8] += b[i]*(2**(i%8))

    return bytes(B)

def BytesToBits(B: bytes):
    C = list(B)
    b = [0] * (8*len(C))
    for i in range(len(C)):
        for j in range(8):
            b[8*i + j] = C[i] % 2
            C[i] = C[i] // 2
    return b

